        private void MAdapterOnDeleteItemClick(object sender, AddressAdapterClickEventArgs e)
        {
            try
            {
                if (Methods.CheckConnectivity())
                {
                    var itemUser = MAdapter.GetItem(e.Position);
                    if (itemUser != null)
                    {
                        var dialog = new MaterialAlertDialogBuilder(this);
                        dialog.SetTitle(GetText(Resource.String.Lbl_DeleteAddress));
                        dialog.SetMessage(GetText(Resource.String.Lbl_AreYouSureDeleteAddress));
                        dialog.SetPositiveButton(GetText(Resource.String.Lbl_Yes), (materialDialog, action) =>
                        {
                            try
                            {
                                var index = MAdapter.AddressList.IndexOf(itemUser);
                                if (index != -1)
                                {
                                    MAdapter.AddressList.Remove(itemUser);
                                    MAdapter.NotifyItemRemoved(index);
                                }
                                ShowEmptyPage();

                                PollyController.RunRetryPolicyFunction(new List<Func<Task>> { () => RequestsAsync.Address.DeleteAddressesAsync(itemUser.Id) });
                            }
                            catch (Exception exception)
                            {
                                Methods.DisplayReportResultTrack(exception);
                            }
                        });
                        dialog.SetNegativeButton(GetText(Resource.String.Lbl_No), new MaterialDialogUtils());

                        dialog.Show();

                    }
                }
                else
                {
                    Toast.MakeText(this, GetText(Resource.String.Lbl_CheckYourInternetConnection), ToastLength.Short)?.Show();
                }
            }
            catch (Exception exception)
            {
                Methods.DisplayReportResultTrack(exception);
            }
        }

        private void MAdapterOnEditItemClick(object sender, AddressAdapterClickEventArgs e)
        {
            try
            {
                var item = MAdapter.GetItem(e.Position);
                if (item != null)
                {
                    var intent = new Intent(this, typeof(EditAddressActivity));
                    intent.PutExtra("ItemData", JsonConvert.SerializeObject(item));
                    StartActivity(intent);
                }
            }
            catch (Exception exception)
            {
                Methods.DisplayReportResultTrack(exception);
            }
        }

